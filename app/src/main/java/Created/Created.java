package Created;

public class Created {
    int a;
    String b;
    double c;

    public Created(int a, String b, double c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public int getA() {
        return a;
    }

    public String getB() {
        return b;
    }

    public double getC() {
        return c;
    }

    public void setA(int a) {
        this.a = a;
    }

    public void setB(String b) {
        this.b = b;
    }

    public void setC(double c) {
        this.c = c;
    }
}


