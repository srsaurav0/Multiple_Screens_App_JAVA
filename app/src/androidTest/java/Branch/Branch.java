package Branch;

public class Branch {
    int p;
    float q;
    String r;
    String s;

    public Branch(int p, float q, String r, String s) {
        this.p = p;
        this.q = q;
        this.r = r;
        this.s = s;
    }

    public String getS() {
        return s;
    }

    public void setS(String s) {
        this.s = s;
    }

    public int getP() {
        return p;
    }

    public void setP(int p) {
        this.p = p;
    }

    public float getQ() {
        return q;
    }

    public void setQ(float q) {
        this.q = q;
    }

    public String getR() {
        return r;
    }

    public void setR(String r) {
        this.r = r;
    }
}
